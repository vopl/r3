#include "pch.h"
#include "async/event.hpp"
#include "eventImpl.hpp"

namespace async
{
	//////////////////////////////////////////////////////////////////////////
	Event::Event(bool autoReset)
		: _impl(new EventImpl(autoReset))
	{
	}


	//////////////////////////////////////////////////////////////////////////
	void Event::set()
	{
		return _impl->set();
	}

	//////////////////////////////////////////////////////////////////////////
	void Event::reset()
	{
		return _impl->reset();
	}

	//////////////////////////////////////////////////////////////////////////
	bool Event::isSet()
	{
		return _impl->isSet();
	}

	//////////////////////////////////////////////////////////////////////////
	void Event::wait()
	{
		return _impl->wait();
	}

	//////////////////////////////////////////////////////////////////////////
	Event *Event::waitAny(Event *begin, Event *end)
	{
		return EventImpl::waitAny(begin, end);
	}



}

